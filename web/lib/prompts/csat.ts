/** 共通テスト形式の問題生成プロンプトを組み立てる */
export function buildCsatPrompt(
    subjectLabel: string,
    units: { id: string; label: string }[],
    questionCount: number,
): string {
    const fiscalYear = getCurrentFiscalYear()
    const unitText =
        units.length > 0
        ? units.map((u) => u.label).join('・')
        : `${subjectLabel}の全単元（ランダム）`

    // 単元ID対応表：AIにはこのidのいずれかを unitId として返させる（ラベル照合に依存しない）。
    const unitIdTable =
        units.length > 0
            ? units.map((u) => `          - ${u.id} ： ${u.label}`).join('\n')
            : '          （単元指定なし。最も適切な単元を自分で判断し、その単元のidは付けず空文字 "" を返す）'

    return `
        あなたは高校${subjectLabel}の共通テスト対策問題を作成する専門家です。

        以下の条件で問題を${questionCount}問生成してください。

        【条件】
        - 対象：高校${subjectLabel}（${fiscalYear}年度時点の最新学習指導要領準拠）
        - 形式：共通テスト形式（4択選択問題）
        - 単元：${unitText}
        - 難易度：共通テスト標準レベル
        - 指定された科目・単元の範囲を厳守すること。範囲外の発展内容を含めない
        - 同じ問題を繰り返さず、毎回異なる問題を生成すること
        - 各問題に、その問題で問われている主要な概念を表す keywords を3〜5個付けること
          （復習の手がかりに使う。例：「平方完成」「頂点」「最大値」のような短い語）

        【問題文(question)の作り方】
        - question には「問い」だけを書く。選択肢A〜Dの文章を question の中に列挙・再掲しないこと
          （選択肢は choices にのみ書く。question に「次のうち〜 ア:… イ:… のどれか」のように
            選択肢本文を重複して書かない）
        - question 内で選択肢に言及する必要があるときも、本文を再掲せず「次の選択肢のうち」「下のうち」
          程度の表現にとどめる
        - プログラミング等でコード・擬似コードを示す場合も、選択肢の中身を question 側に書かないこと
        - 【NG例】question を次のように書いてはいけない（選択肢本文の列挙が混入している悪い例）:
            「変数の役割として最も適切なものを選べ。
             （１）実行順序を制御する命令。 （２）値を記憶する箱。 （３）注釈。 （４）終了指示。」
          → 正しくは question は「変数の役割として最も適切なものを次のうちから一つ選べ。」のみとし、
            （１）〜（４）の各文は choices に入れる。

        【選択肢(choices)の作り方】
        - 各選択肢の本文の先頭に、番号や記号（（１）(1) ① 1. ア． A. 等）を付けないこと。
          choices には「選択肢の文そのもの」だけを入れる。番号は付けない
          （表示時にアプリ側が自動で振る。本文に番号を含めると二重表示になる）
        - 【NG例】「（１）値を記憶する箱。」「①値を記憶する箱。」のように番号付きで書いてはいけない。
          → 正しくは「値を記憶する箱。」のように本文のみにする
        - A〜Dの4つの選択肢は、すべて内容の異なる別の文にすること。同じ文・実質的に同一の選択肢を
          2つ以上作らないこと（語順や言い回しだけ変えた実質同一も不可）
        - 正解はちょうど1つにすること。複数の選択肢が同時に正解になる作問をしないこと
        - 誤答（ダミー）は、ありがちな誤解に基づくもっともらしい別内容にし、正解の単純な重複にしない

        【解説(explanation)の作り方】
        - 文体は共通テストの解説にふさわしい簡潔な敬体（です・ます）で書く。口語・雑談・感想・
          呼びかけ（「〜だよね」「考えてみよう」「面白いですね」等）を使わないこと
        - 長さは目安として日本語で120〜250字程度。冗長な前置き・繰り返し・蛇足を入れず、
          「なぜその選択肢が正解か」を中心に簡潔にまとめる
        - 1つの解説で説明する論点は1〜2点に絞る。手順を列挙する場合も要点のみとし、
          長大なステップbyステップや余談を避ける
        - 誤答選択肢の解説は、必要な場合のみ1行程度で簡潔に触れる（全選択肢を逐一長く論評しない）

        【単元ID対応表（unitId は必ずこの id のいずれかを使う）】
${unitIdTable}
        - 各問題の unitId には、上の表の id（左側の英数字。例 bio_base_02）を**そのままの表記で**入れること
        - 単元名（ラベル）ではなく id を入れること。表に無い id は使わないこと
        - 複数単元が指定されている場合は、その問題が実際に該当する単元の id を選ぶこと

        【記法の厳守事項（重要）】
${NOTATION_RULES}

        【出力形式】
        必ず以下のJSON形式のみで出力すること。
        前後に説明文・コードブロック記号は不要。すべての文字列値はダブルクォートで囲むこと。

        ${CSAT_OUTPUT_SCHEMA}
    `.trim()
}